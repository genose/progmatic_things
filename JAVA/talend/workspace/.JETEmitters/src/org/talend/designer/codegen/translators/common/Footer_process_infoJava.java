package org.talend.designer.codegen.translators.common;

import org.talend.designer.codegen.config.CodeGeneratorArgument;
import java.text.DateFormat;
import java.util.Calendar;
import org.talend.core.ui.branding.IBrandingService;
import org.talend.core.GlobalServiceRegister;

public class Footer_process_infoJava
{
  protected static String nl;
  public static synchronized Footer_process_infoJava create(String lineSeparator)
  {
    nl = lineSeparator;
    Footer_process_infoJava result = new Footer_process_infoJava();
    nl = null;
    return result;
  }

  public final String NL = nl == null ? (System.getProperties().getProperty("line.separator")) : nl;
  protected final String TEXT_1 = "/************************************************************************************************" + NL + " *     ";
  protected final String TEXT_2 = " characters generated by ";
  protected final String TEXT_3 = " " + NL + " *     on the ";
  protected final String TEXT_4 = NL + " ************************************************************************************************/";

  public String generate(Object argument)
  {
    final StringBuffer stringBuffer = new StringBuffer();
    
	CodeGeneratorArgument codeGenArgument = (CodeGeneratorArgument) argument;
    Integer chars = (Integer) codeGenArgument.getArgument();

    stringBuffer.append(TEXT_1);
    stringBuffer.append(chars );
    stringBuffer.append(TEXT_2);
    stringBuffer.append(((IBrandingService) GlobalServiceRegister.getDefault().getService(
                IBrandingService.class)).getFullProductName() );
    stringBuffer.append(TEXT_3);
    stringBuffer.append(DateFormat.getDateTimeInstance(DateFormat.LONG,DateFormat.LONG).format(Calendar.getInstance().getTime()) );
    stringBuffer.append(TEXT_4);
    return stringBuffer.toString();
  }
}
